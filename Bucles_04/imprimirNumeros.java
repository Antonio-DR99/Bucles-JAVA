package ED_04.Bucles_04;
/*
 * Escribe un programa que imprima los numeros del 1 al 10
 * usando bucle for
 */
public class imprimirNumeros {
    public static void main(String[] args) {

        for (int num =1; num<=10; num++){
            System.out.println(num);
        }
    }
    
}
